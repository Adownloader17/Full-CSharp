// Import the System namespace which contains fundamental classes like Console
using System;

// Declare a class named Program - all code in C# must be inside a class or struct
class Program
{
    // This is the Main method - the entry point of a C# application
    // The program starts executing from here
    static void Main()
    {
        // This line outputs text to the console
        Console.WriteLine("Hello, World!");
    }
}


| Code Part                | Meaning                                                                  |
| ------------------------ | ------------------------------------------------------------------------ |
| `using System;`          | Includes the System namespace so you can use `Console` and other classes |
| `class Program`          | Defines a class called `Program`                                         |
| `static void Main()`     | Main method where program execution begins                               |
| `Console.WriteLine(...)` | Prints a line of text to the console                                     |
